		const autoSliderFn = () => {
			this.scroll = true;
			this.isWait = true;
			wait = setTimeout(() => {
				this.isWait = false;
			}, 1200);
			this.activeSlide = this.activeSlide + 1 === this.slides.length ? 0 : this.activeSlide + 1;
			this.prevSlide = this.activeSlide - 1 === -1 ? this.slides.length - 1 : this.activeSlide - 1;
